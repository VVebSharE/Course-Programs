#6. Write a Program to illustrate common string operations in python.
str1=input("Enter str1 :")
str2=input("Enter str2 :")
#concatination
print(f"str1 + str2 :{str1+str2}")
print(f"str1*4 :{str1*4}")
#membership
print(f"str1 in str2 :{str1 in str2}")
#comparison
print(f"str1 == str2 :{str1 == str2}")
print(f"str1 != str2 :{str1 != str2}")
print(f"str1 < str2 :{str1 < str2}")
print(f"str1 > str2 :{str1 > str2}")
print(f"str1 <= str2 :{str1 <= str2}")
print(f"str1 >= str2 :{str1 >= str2}")
#build in functions on string
print(f"len(str1) :{len(str1)}")
print(f"max(str1) :{max(str1)}")
print(f"min(str1) :{min(str1)}")